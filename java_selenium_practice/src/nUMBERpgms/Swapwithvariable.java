package nUMBERpgms;

public class Swapwithvariable {

	public static void main(String[] args) {
		System.out.println("before swapping");
		int a=10;
		int b=20;
		int t=0;
		System.out.print(a);
		System.out.print(b);
		System.out.println();
		System.out.println("after Swapping");
		t=a;
		a=b;
		b=t;
		System.out.print(a);
		System.out.print(b);

	}

}
